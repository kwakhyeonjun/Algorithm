import java.util.Scanner;

public class B10951 {
    public static void main(String[] args){
        Scanner sc = new Scanner(System.in);
        int A, B;
        while(sc.hasNext()){
            A = sc.nextInt();
            B = sc.nextInt();
            System.out.println(A+B);
        }
    }
}
